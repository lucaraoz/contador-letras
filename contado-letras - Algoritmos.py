#Sentencia = problema sencillo
#ALGORITMOS: serie de pasos secuneciales para poder resolver una problemática de forma eficiente.

#Paso 1: pedir al usuario que ingrese una palabra para poder contar sus letras
palabra_ingresada = input("Ingresá una palabra para contar sus letras: ")

#Paso 2: contar la cantidad de letras
cantidad_letras = len(palabra_ingresada)

#Paso 3: mostrar al usuario la cantidad de letras.
print("La palabra ingresada tiene", cantidad_letras, "letras.")

